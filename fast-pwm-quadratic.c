int main(void) {

    // pulse width of OC1A
    unsigned char widtha = 0;
    unsigned char direction = 0;

    // PWM output on PB3(OC1A) and PB4(OC1B)
    // PB0 - direction indicator
    DDRB |= _BV(PB3) | _BV(PB4) | _BV(PB0);

    TCCR1A = _BV(COM1A1) | _BV(COM1B1) | _BV(WGM11) | _BV(WGM10);
    TCCR1B = _BV(CS11);

    while(1) {
        if ((255 == widtha) || (0 == widtha)) {
            direction ^= 1;
            if (direction) {
                PORTB |= _BV(PB0);
            } else {
                PORTB &= ~(_BV(PB0));
            }
        }
        _delay_ms(5);
        OCR1A = pgm_read_byte( &(quadratic[widtha]) );
        OCR1B = pgm_read_byte( &(quadratic[255 - widtha]) );

        if (direction) {
            widtha++;
        } else {
            widtha--;
        }
   }
}
